public class Main {
    public static void main(String[] args) {
        Stack<Integer> stack = new Stack<>(); //Создаём стек целых чисел
        
        //Добавляем элементы в стек
        stack.push(1);
        stack.push(2);
        stack.push(3);
        
        System.out.println(stack.pop()); // Извлекаем и печатаем последний элемент (3)
        System.out.println(stack.peek()); // Печатаем верхний элемент без извлечения (2)
        System.out.println(stack.pop()); // Извлекаем и печатаем последний элемент(2)
        System.out.println(stack.isEmpty()); // Проверяем, пуст ли стек (false)
        System.out.println(stack.pop()); // Извлекаем и печатаем последний элемент (1)
        System.out.println(stack.isEmpty()); // Проверяем, пуст ли стек (true)
    }
}
